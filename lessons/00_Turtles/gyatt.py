import turtle as turtle

screen = turtle.Screen()
screen.setup(width=600, height=600)
screen.bgcolor('white')

t1 = turtle.Turtle()
t1.penup()
t1.shape("turtle")

t2 = turtle.Turtle()
t2.penup()
t2.shape("arrow")


for i in range(-200, 200):
    t1.goto(i,i)
    t2.goto(i,-i)

























































































































































































































































