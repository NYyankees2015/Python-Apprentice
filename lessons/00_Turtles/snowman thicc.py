import turtle
turtle.setup(width=600, height=600)
tina = turtle.Turtle()                  

tina.shape('turtle')                   
tina.speed(10)


tina.goto(0,100)

tina.circle(100)

tina.goto(0, -150)

tina.circle(125)

tina.goto(0, -120)





















































































































































































































































turtle.exitonclick()